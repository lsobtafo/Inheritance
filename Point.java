package shapes;
import java.awt.geom.Point2D;


public class Point {
	private Point2D.Double x;
	private Point2D.Double y;
	public Point() {//Point2D.Double x, Point2D.Double y
		this.x = x;
		this.y = y;
	}
	public Point2D.Double getX() {
		return x;
	}
	public void setX() {//Point2D.Double x
		this.x = x;
	}
	public Point2D.Double getY() {//Point2D.Double y
		return y;
	}
	public void setY() {
		this.y = y;
	}

}
